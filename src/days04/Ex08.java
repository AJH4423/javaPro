package days04;

public class Ex08 {
	
	public static void main(String[] args) {
		
		// for문/ foreach문: 반복문
		// 동일한 작업을 반복할때 반복문 사용
		// for ( 초기식; 조건식; 증감식) {}
		
		System.out.println("test");
		System.out.println("test2");
		System.out.println("test");
		System.out.println("test");
		
		for (byte i = 1; i <= 10; i--) {
			System.out.printf("안준호: %d\n", i);
		} // for i
		
		System.out.println("test");
		System.out.println("test");
		System.out.println("test7");
		System.out.println("test");

		
	} // main

}// class
 